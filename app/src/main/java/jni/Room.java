package jni;

public class Room {
  private int id;
  private String name;
  private String description;
  private String created;

  public int getId() {
    return id;
  }

  public String getName() {
    return name;
  }

  public String getDescription() {
    return description;
  }

  public String getCreated() {
    return created;
  }
}
